  data () {
    return {
      today: new Date(),
      calendar: ['Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', 'Sunday', 'This Month', 'Next Month']
    }
  },
  computed: {
    mondayOfCurrentWeek () {
      const first = this.today.getDate() - this.today.getDay() + 1
      const monday = new Date(this.today.setDate(first))

      return monday
    },
    list () {
